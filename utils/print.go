package utils

import "fmt"

/*
组成部分解释

\033
这是 ANSI 转义序列的起始标志（也称为 ESC 字符）。
它表示接下来是一个控制序列，用于更改终端的显示样式。
[48;2;125;125;125;30m

48: 设置背景颜色。
;2: 表示使用 24 位 RGB 模式（即 TrueColor 模式）。
125;125;125: 这是背景颜色的 RGB 值，分别对应红色 (R)、绿色 (G)、蓝色 (B)。
在这里，125, 125, 125 表示一种中等灰色背景。
30: 设置前景颜色（文字颜色），30 表示黑色文字。
m: 终止转义序列并应用指定的样式。
%s

占位符，表示将要插入的字符串内容。
line

被插入到 %s 的变量。在此代码中，line 是当前需要输出的一行字符串。
\033[0m

重置所有样式，恢复到终端的默认显示格式。
如果没有这个重置，后续输出可能会继续沿用之前的样式，导致意外的格式问题。

\n
表示换行符，用于将输出内容换到下一行。
*/
// 控制台带颜色打印
func ColorPrint(line string) {
	fmt.Printf("\033[48;2;125;125;125;30m%s\033[0m\n", line)
}
