// @lc code=start
func strStr(haystack string, needle string) int {
	index := -1
	if needle == "" {
		return 0
	}
	for i := 0; i+len(needle) <= len(haystack); i++ {
		if haystack[i:i+len(needle)] == needle {
			return i
		}
	}
	return index
}

